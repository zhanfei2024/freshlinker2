function processor(data) {
  let parent = [];
  let children = [];
  data.forEach((val)=> {
    parent.push(_.omit(val,'children'));
    val.children.forEach((child)=>{
      children.push(Object.assign(child,{'parentId':val.id}));
    });
  });
  return [parent,children]
}

function importData(model,data) {
  return new Promise((resolve,reject)=>{
    debug(`Imported ${model.name} data!`);
    resolve(model.bulkCreate(data,{returning: true}));
  });
}
